export type OpeningUtteranceMode = "first" | "reconnect";

export function buildOpeningUtterance(
  context?: InterviewStartContext,
  mode: OpeningUtteranceMode = "first"
): string {
  const candidateFullName = getCandidateDisplayName(context);
  const company = context?.companyName?.trim() || "компания не указана";
  const jobTitle = context?.jobTitle?.trim() || "должность не указана";
  const customGreeting = context?.greetingSpeech?.trim();
  const hasCompany = company !== "компания не указана";
  const hasTitle = jobTitle !== "должность не указана";
  const selfIntroLine = buildAgentSelfIntroLine(context);

  /**
   * Reconnect-aware intro: после восстановления WebRTC модель уже один раз произнесла
   * полный JobAI greeting; повторное приветствие звучит как баг. Озвучиваем только
   * нейтральный мост к продолжению.
   */
  if (mode === "reconnect") {
    return [
      selfIntroLine,
      "Связь была восстановлена, продолжаем собеседование с того же места."
    ].join("\n\n");
  }

  if (customGreeting) {
    /**
     * Кастомный greetingSpeech из JobAI — единственный «истинный» текст приветствия.
     * Никаких contextBridge / повторов: иначе модель уходит в перефраз и теряет блок про запись.
     */
    const script = resolveJobAiScriptPlaceholders(customGreeting, context);
    return [selfIntroLine, script].join("\n\n");
  }
  return [
    selfIntroLine,
    `Здравствуйте, ${candidateFullName}. Когда будете готовы перейти к вопросам, скажите — или коротко уточните, если нужно что-то про формат.`,
    `Вы готовы начать интервью по вакансии ${hasTitle ? `«${jobTitle}»` : "этой позиции"}${hasCompany ? ` в ${company}` : ""}?`
  ].join("\n\n");
}
